package Numerical;

public class Arithmetic {
    public static void main(String[]args){
        float number1 = 1.3f;
        double number2 = 2.5d;
        int number3 = 6;
        byte number4 = 2;
        long number5 = 5684;
        short number6 = 45;
        System.out.println(number1/number3);
        System.out.println(number2/number3);
        System.out.println(number3/number4);
        System.out.println(number4/number6);
    }
}
